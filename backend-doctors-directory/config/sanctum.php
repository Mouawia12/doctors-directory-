<?php

use Laravel\Sanctum\Sanctum;

return [

    /*
    |--------------------------------------------------------------------------
    | Stateful Domains
    |--------------------------------------------------------------------------
    |
    | Requests from the following domains / hosts will receive stateful API
    | authentication cookies. Typically, these should include your local
    | and production domains which access your API via a frontend SPA.
    |
    */

    'stateful' => (function () {
        $explicit = env('SANCTUM_STATEFUL_DOMAINS');

        if ($explicit) {
            return array_filter(array_map('trim', explode(',', $explicit)));
        }

        $defaults = [
            'localhost',
            'localhost:3000',
            'localhost:5173',
            'localhost:5174',
            '127.0.0.1',
            '127.0.0.1:3000',
            '127.0.0.1:8000',
            '::1',
        ];

        $frontendUrls = array_filter(array_map('trim', explode(',', env('FRONTEND_URLS', env('FRONTEND_URL', '')))));

        $normalize = static function (string $url): ?string {
            if ($url === '') {
                return null;
            }

            $host = parse_url($url, PHP_URL_HOST);
            $port = parse_url($url, PHP_URL_PORT);

            if (! $host) {
                return trim($url);
            }

            return $port ? "{$host}:{$port}" : $host;
        };

        foreach ($frontendUrls as $url) {
            $value = $normalize($url);
            if ($value) {
                $defaults[] = $value;
            }
        }

        if ($appUrl = Sanctum::currentApplicationUrlWithPort()) {
            $value = $normalize($appUrl);
            if ($value) {
                $defaults[] = $value;
            }
        }

        return array_values(array_unique(array_filter($defaults)));
    })(),

    /*
    |--------------------------------------------------------------------------
    | Sanctum Guards
    |--------------------------------------------------------------------------
    |
    | This array contains the authentication guards that will be checked when
    | Sanctum is trying to authenticate a request. If none of these guards
    | are able to authenticate the request, Sanctum will use the bearer
    | token that's present on an incoming request for authentication.
    |
    */

    'guard' => ['web'],

    /*
    |--------------------------------------------------------------------------
    | Expiration Minutes
    |--------------------------------------------------------------------------
    |
    | This value controls the number of minutes until an issued token will be
    | considered expired. This will override any values set in the token's
    | "expires_at" attribute, but first-party sessions are not affected.
    |
    */

    'expiration' => null,

    /*
    |--------------------------------------------------------------------------
    | Token Prefix
    |--------------------------------------------------------------------------
    |
    | Sanctum can prefix new tokens in order to take advantage of numerous
    | security scanning initiatives maintained by open source platforms
    | that notify developers if they commit tokens into repositories.
    |
    | See: https://docs.github.com/en/code-security/secret-scanning/about-secret-scanning
    |
    */

    'token_prefix' => env('SANCTUM_TOKEN_PREFIX', ''),

    /*
    |--------------------------------------------------------------------------
    | Sanctum Middleware
    |--------------------------------------------------------------------------
    |
    | When authenticating your first-party SPA with Sanctum you may need to
    | customize some of the middleware Sanctum uses while processing the
    | request. You may change the middleware listed below as required.
    |
    */

    'middleware' => [
        'authenticate_session' => Laravel\Sanctum\Http\Middleware\AuthenticateSession::class,
        'encrypt_cookies' => Illuminate\Cookie\Middleware\EncryptCookies::class,
        'validate_csrf_token' => Illuminate\Foundation\Http\Middleware\ValidateCsrfToken::class,
    ],

];
